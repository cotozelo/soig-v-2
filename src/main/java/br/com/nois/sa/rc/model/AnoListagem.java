package br.com.nois.sa.rc.model;

public class AnoListagem {

	private String editar;
	private String exibir;
	private String ano;
	private String instituicaoId;

	public String getEditar() {
		return editar;
	}

	public void setEditar(String editar) {
		this.editar = editar;
	}

	public String getExibir() {
		return exibir;
	}

	public void setExibir(String exibir) {
		this.exibir = exibir;
	}

	public String getAno() {
		return ano;
	}

	public void setAno(String ano) {
		this.ano = ano;
	}

	public String getInstituicaoId() {
		return instituicaoId;
	}

	public void setInstituicaoId(String instituicaoId) {
		this.instituicaoId = instituicaoId;
	}
}
