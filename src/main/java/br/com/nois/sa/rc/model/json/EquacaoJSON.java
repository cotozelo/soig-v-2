package br.com.nois.sa.rc.model.json;

public class EquacaoJSON {
	private String ano;
	private String equacaoId;
	private String formula;
	private String indicadorId;
	private String indicadorSigla;

	public String getAno() {
		return ano;
	}

	public void setAno(String ano) {
		this.ano = ano;
	}

	public String getEquacaoId() {
		return equacaoId;
	}

	public void setEquacaoId(String equacaoId) {
		this.equacaoId = equacaoId;
	}

	public String getFormula() {
		return formula;
	}

	public void setFormula(String formula) {
		this.formula = formula;
	}

	public String getIndicadorId() {
		return indicadorId;
	}

	public void setIndicadorId(String indicadorId) {
		this.indicadorId = indicadorId;
	}

	public String getIndicadorSigla() {
		return indicadorSigla;
	}

	public void setIndicadorSigla(String indicadorSigla) {
		this.indicadorSigla = indicadorSigla;
	}
}
